class FourCal:
    def setdata(self, first, second):
        self.first = first
        self.second = second
    
    def add(self):
        result = self.first + self.second
        return result
    
    def mul(self):
        return self.first * self.second
    
    def div(self):
        return self.first // self.second
    
    def sub(self):
        return self.first - self.second
        
